# 산술 연산자
# + - * / //(몫) %(나머지) **(거듭제곱)

print(3 + 5)  # 8
print(10 - 4)
print(4 * 5)
print(20 / 4)  # 5.0 -> 나누기는 float 주의
print(7 // 3)  # 2
print(7 % 3)  # 1
print(2**5)  # 2의 5제곱

# ====================================================

# 연산 우선 순위와 우선순위 지정
# 거듭제곱 > 곱하기/나누기/몫/나머지 동일 >  더하기/빼기

print(2 + 8 * 3)  # 곱하기 먼저 계산 후 그 결과를 2와 더한다.

print((2 + 8) * 3)

# ===================================================

# 복합연산자

result = 3 * 5
print(result)

# +=: 기존 값에서 오른쪽 값을 더한 뒤 재할당
# -=: 기존 값에서 오른쪽쪽 값을 뺸 뒤 재할당
# *=: 기존 값에서 오른쪽 값을 곱한 뒤 재할당
# /=: 기존 값에서 오른쪽 값을 나눈 뒤 재할당

result += 10  # 25
print(result)
result -= 5  # 20
print(result)
result *= 3  # 60
print(result)
result /= 2  # 30.0
print(result)


