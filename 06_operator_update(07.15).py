# 문자열 연산
print("안녕" + "하세요")

# 안녕과 하세요 사이에 공백을 1개 넣고 싶다면
# 방법1) ,사용
print("안녕", "하세요")
# 방법2) 안녕 뒤에 공백 추가
print("안녕 " + "하세요")
# 방법3) 공백만 있는 문자열 더하기
print("안녕" + " " + "하세요")

# 문자열 곱하기
print("안녕" * 5)

